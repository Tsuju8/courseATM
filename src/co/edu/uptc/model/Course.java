package co.edu.uptc.model;

public class Course {
	private String name;
	private String info;
		
	public Course(String name, String info) {
		super();
		this.name = name;
		this.info = info;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}
	
	
	
	
	
}
